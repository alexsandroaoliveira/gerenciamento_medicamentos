CREATE TABLE FABRICANTE (
    ID_FABRICANTE INT NOT NULL,
    NOME VARCHAR(100),
    CONSTRAINT PK_FABRICANTE PRIMARY KEY (ID_FABRICANTE)
);

CREATE TABLE MEDICAMENTO (
    ID_MEDICAMENTO INTEGER NOT NULL,
    NUM_ANVISA VARCHAR(17) NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    DATA_VALIDADE DATE,
    TELEFONE_SAC VARCHAR(13),
    PRECO DECIMAL,
    QUANTIDADE_COMPRIMIDO INT,
    ID_FABRICANTE INT,
    CONSTRAINT PK_MEDICAMENTO PRIMARY KEY (ID_MEDICAMENTO),
    CONSTRAINT FK_MEDICAMENTO_FABRICANTE FOREIGN KEY (ID_FABRICANTE) REFERENCES FABRICANTE(ID_FABRICANTE)
);

CREATE TABLE REACAO_ADVERSA (
    ID_REACAO INTEGER NOT NULL,
    DESCRICAO VARCHAR(255),
    CONSTRAINT PK_REACAO_ADVERSA PRIMARY KEY (ID_REACAO)
);

CREATE TABLE MEDICAMENTO_REACAO (
    ID_MEDICAMENTO INTEGER NOT NULL,
    ID_REACAO INTEGER NOT NULL,
    CONSTRAINT PK_MEDICAMENTO_REACAO PRIMARY KEY (ID_MEDICAMENTO, ID_REACAO),
    CONSTRAINT FK_M_REACAO_REACAO FOREIGN KEY (ID_REACAO) REFERENCES REACAO_ADVERSA(ID_REACAO),
    CONSTRAINT FK_M_REACAO_MEDICAMENTO FOREIGN KEY (ID_MEDICAMENTO) REFERENCES MEDICAMENTO(ID_MEDICAMENTO)
);

